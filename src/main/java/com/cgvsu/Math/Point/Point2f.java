package com.cgvsu.Math.Point;

public class Point2f {
    private double x;
    private double y;

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    public Point2f(double x, double y) {
        this.x = x;
        this.y = y;
    }
}
